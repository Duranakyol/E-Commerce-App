const mongoose = require(mongoose);

const produktSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
  },
  price: {
    type: Number,
    required: true,
  },
  photos: [String],
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

const Produkt = mongoose.model("Produkt", produktSchema);

module.exports = Produkt;
